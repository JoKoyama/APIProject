package com.example.apiproject;

public class Orginaizer {
    private String title;
    private String genre;
    private String episodes;
    private String airingSeason;
    private String producer;
    private String animationStudio;
    private String synopsis;
    private String malScore;
    private String status;
    private String statusString;

    public Orginaizer(){

    }


    public String getTitle() { return title; }
    public void setTitle(String title) { this.title = title; }
    public String getGenre() { return genre; }
    public void setGenre(String genre) { this.genre = genre; }
    public String getEpisodes() { return episodes; }
    public void setEpisodes(String episodes) { this.episodes = episodes; }
    public String getAiringSeason() { return airingSeason; }
    public void setAiringSeason(String airingSeason) { this.airingSeason = airingSeason; }
    public String getProducer() { return producer; }
    public void setProducer(String producer) { this.producer = producer; }
    public String getAnimationStudio() { return animationStudio; }
    public void setAnimationStudio(String animationStudio) { this.animationStudio = animationStudio; }
    public String getSynopsis() { return synopsis; }
    public void setSynopsis(String synopsis) { this.synopsis = synopsis; }
    public String getMalScore() { return malScore; }
    public void setMalScore(String malScore) { this.malScore = malScore; }
    public String getStatus() { return status; }
    public void setStatus(String status) { this.status = status; }
    public String getStatusString() { return statusString; }
    public void setStatusString(String statusString) { this.statusString = statusString; }
}
